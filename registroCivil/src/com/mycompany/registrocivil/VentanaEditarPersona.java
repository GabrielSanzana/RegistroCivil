private boolean editando = false;
private Persona personaEditada;
    private void btnBuscarMousePressed(java.awt.event.MouseEvent evt) throws EditarPersonaException{//GEN-FIRST:event_btnBuscarMousePressed
        Object selectedItem = comboBoxRegiones.getSelectedItem();
        Date fechaNacimiento;
        SimpleDateFormat formatoFecha;
        String selectedRegion, fNac; 
        selectedRegion = selectedItem.toString();
        String rut = casillaRut.getText();
        if(selectedRegion.equals("Seleccione") == false)
            personaEditada = conjuntoRegiones.buscarPersona(rut, selectedRegion);
        else
            personaEditada = conjuntoRegiones.buscarPersona(rut);
        
        if (rut.equals("") || personaEditada == null){
            casillaRut.setText("");
            comboBoxRegiones.setSelectedIndex(0);
            casillaNombre.setText("");
            casillaRutDatos.setText("");
            casillaNacimiento.setDate(null);
            casillaDefuncion.setDate(null);
            casillaEstado.setSelectedIndex(0);
            editando = false;
            throw new EditarPersonaException(rut,personaEditada);
        }
        
        editando = true;
        casillaRut.setText("");
        comboBoxRegiones.setSelectedIndex(0);
        casillaNombre.setText(personaEditada.getNombre());
        casillaRutDatos.setText(personaEditada.getRut());
        fNac = personaEditada.getFNac();
        formatoFecha = new SimpleDateFormat("yyyy-MM-dd");
        try {
            fechaNacimiento = formatoFecha.parse(fNac);
            casillaNacimiento.setDate(fechaNacimiento);
        } catch (ParseException e) {
            e.printStackTrace(); 
        }
        if(!personaEditada.getDef().equals("Sin fallecimiento reportado"))
            try {
                casillaDefuncion.setDate(formatoFecha.parse(personaEditada.getDef()));
            } catch (ParseException e) {
                e.printStackTrace(); 
            }
        int estadoNumerico = 0;
            if (personaEditada.getEstadoCivil().equals("Soltero/a")) 
                estadoNumerico = 0;
            else if (personaEditada.getEstadoCivil().equals("Casado/a")) 
                estadoNumerico = 1;
            else if (personaEditada.getEstadoCivil().equals("Divorciado/a")) 
                estadoNumerico = 2;
            else if (personaEditada.getEstadoCivil().equals("Viudo/a")) 
                estadoNumerico = 3;
            else if (personaEditada.getEstadoCivil().equals("Separado/a")) 
                estadoNumerico = 4;
        casillaEstado.setSelectedIndex(estadoNumerico);
        casillaEstado.setEnabled(true);
        casillaNombre.setEnabled(true);
        casillaDefuncion.setEnabled(true);
    }//GEN-LAST:event_btnBuscarMousePressed

