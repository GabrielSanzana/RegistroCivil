    private void btnGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGuardarMousePressed
        if(editando != true)
        {
            JOptionPane.showMessageDialog(null, "No se proporcionó un nombre válido para editar.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        else
        {
            String nombre = casillaNombre.getText();       
            SimpleDateFormat formatoFecha;  
            Date fechaSeleccionada;
            String fechaFormateada;
            Date fechaNacimiento = null;
            formatoFecha = new SimpleDateFormat("yyyy-MM-dd");

            String estado = casillaEstado.getSelectedItem().toString();
            
            int estadoNumerico = 0;
            
            if( (estado.equals("Soltero/a") && !personaEditada.getEstadoCivil().equals("Soltero/a") ) || nombre.equals(""))
            {
                JOptionPane.showMessageDialog(null, "Los datos ingresados son incorrectos o incompletos. Por favor, verifique los datos e intente nuevamente.", "Error de entrada de datos", JOptionPane.ERROR_MESSAGE);
                return;
            }      
            
            if (estado.equals("Casado/a")) 
                estadoNumerico = 1;
            else if (estado.equals("Divorciado/a")) 
                estadoNumerico = 2;
            else if (estado.equals("Viudo/a")) 
                estadoNumerico = 3;
            else if (estado.equals("Separado/a")) 
                estadoNumerico = 4;
            if(casillaDefuncion.getDate()!= null)
            {
                fechaSeleccionada = casillaDefuncion.getDate();
                fechaFormateada = formatoFecha.format(fechaSeleccionada);
                if(!personaEditada.getFNac().equals("Sin fallecimiento reportado"))
                {
                    try {
                        fechaNacimiento = formatoFecha.parse(personaEditada.getFNac());
                    }catch (ParseException e) {
                        e.printStackTrace(); 
                    }
                    if(fechaSeleccionada.before(fechaNacimiento)){
                        JOptionPane.showMessageDialog(null, "Los datos ingresados son incorrectos o incompletos. Por favor, verifique los datos e intente nuevamente.", "Error de entrada de datos", JOptionPane.ERROR_MESSAGE);
                        return;
                    }
                }
            }
            else
                fechaFormateada = "Sin fallecimiento reportado";
            personaEditada.setNombre(nombre);
            personaEditada.setEstadoCivil(estadoNumerico);
            personaEditada.setDefuncion(fechaFormateada);
            editando = false;
            casillaEstado.setEnabled(false);
            casillaRut.setText("");
            comboBoxRegiones.setSelectedIndex(0);
            casillaNombre.setText("");
            casillaRutDatos.setText("");
            casillaNacimiento.setDate(null);
            casillaDefuncion.setDate(null);
            casillaEstado.setSelectedIndex(0);
            casillaEstado.setEnabled(false);
            casillaNombre.setEnabled(false);
            conjuntoRegiones.exportarXlsTodasLasRegiones();
            conjuntoRegiones.exportarXlsVotantesTodasLasRegiones();
            JOptionPane.showMessageDialog(null, "Se ha editado exitosamente a la persona: " + nombre, "Edición Exitosa", JOptionPane.INFORMATION_MESSAGE);
        }
            
    }//GEN-LAST:event_btnGuardarMousePressed

